    private void btnCadaCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadaCategoriaActionPerformed
        if(validarCampos()){
            Cliente cliente = new Cliente();
            cliente.setNome(nomeCadPFisica.getText());
            cliente.setEndereco(enderecoCadPFisica.getText());

            PessoaFisica pessoa = new PessoaFisica();
            pessoa.setSexo(sexo);
            pessoa.setDataNascimento(formatarData(nascCadPFisica.getText()));
            String cpf = idCadCateg.getText();
            cpf = cpf.replace(".", "");
            cpf = cpf.replace("-", "");
            pessoa.setCpf(cpf);
            if (service.buscarCPF(cpf)){
                JOptionPane.showMessageDialog(null, "CPF j√° cadastrado");
            }
            else{
                try {
                    if (service.cadClientePFisica(cliente, pessoa)){
                        limparCampos();
                        JOptionPane.showMessageDialog(null, "Cliente Cadastrado!");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Erro ao realizar cadastro.");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro inesperado" + ex);
                }
            }
        }
    }//GEN-LAST:event_btnCadaCategoriaActionPerformed

