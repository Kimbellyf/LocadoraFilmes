package cliente.model;

public class Cliente {
    private int id;
    private String nome;
    private String number;


    public int getId() {
            return id;
    }
    public void setId(int id) {
            this.id = id;
    }
    public String getNome() {
            return nome;
    }
    public void setNome(String nome) {
            this.nome = nome;
    }
    public String getNumber() {
            return number;
    }
    public void setNumber(String number) {
            this.number = number;
    }

}
